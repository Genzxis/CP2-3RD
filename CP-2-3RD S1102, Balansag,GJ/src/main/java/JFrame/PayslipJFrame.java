         int row = jTable1.getSelectedRow();
         TableModel model = jTable1.getModel();
         
            String employeeNumber = getValue(model, row, 0);
            String lastName = getValue(model, row, 1);
            String firstName = getValue(model, row, 2);

        
        jLabel10.setText(employeeNumber);
