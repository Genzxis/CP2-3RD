    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:

        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        String searchTerm = jTextField20.getText().trim();
        if (!searchTerm.isEmpty()) {

            boolean found = false;
            for (int i = 0; i < model.getRowCount(); i++) {

                String empNumber = model.getValueAt(i, 0).toString();
                String lastName = model.getValueAt(i, 1).toString();
                String firstName = model.getValueAt(i, 2).toString();

                if (empNumber.equalsIgnoreCase(searchTerm) ||

                    lastName.equalsIgnoreCase(searchTerm) ||
                    firstName.equalsIgnoreCase(searchTerm)) {
                    jTable1.getSelectionModel().setSelectionInterval(i, i);
                    jTable1.scrollRectToVisible(jTable1.getCellRect(i, 0, true));
                    found = true;
                    break;
                }
            }

            if (!found) {
                JOptionPane.showMessageDialog(this, "Employee not found!", "Search Result", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Please enter a term to search", "Search Result", JOptionPane.WARNING_MESSAGE);
        }

    }//GEN-LAST:event_jButton6ActionPerformed

    private void jComboBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox1ActionPerformed
   
            int selectedRow = jTable1.getSelectedRow();
            if (selectedRow == -1) {
                JOptionPane.showMessageDialog(this, "Please select an employee from Table 1 first!");
                return;
            }

           
            String selectedEmployee = jTable1.getValueAt(selectedRow, 0).toString();

            
            int selectedMonth = jComboBox1.getSelectedIndex() + 1; // jComboBox index starts at 0

           
            DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
            model.setRowCount(0);

            
            String csvFile = "D:\\Motorph\\Computer-Programming-2-master\\src\\main\\java\\JFrame\\AR - Attendance Record.csv";
            String line;
            String cvsSplitBy = ",";
            
            try (BufferedReader br = new BufferedReader(new FileReader(csvFile))) {
                while ((line = br.readLine()) != null) {
                    
                    String[] data = line.split(cvsSplitBy);
                    if (data.length >= 4) { // ensure at least 4 columns exist sa CSV na AR
                        
                        String employeeNumber = data[0].trim(); //employee number kase sa index 0
                        
                        if (employeeNumber.equals(selectedEmployee)) {
                            String date = data[1].trim(); //according sa csv, index 1 ay nagcocontain ng date
                            String[] dateParts = date.split("/"); //split ang date by / kase ganon nakalagay sa csv mm/dd/yyyy
                            if (dateParts.length >= 1) {
                                
                                
                                try {
                                    int month = Integer.parseInt(dateParts[0]); //kase nga nagsisimula siya sa January = 0 index sa jcombo box
                                    if (month == selectedMonth) {
                                        model.addRow(data); // Add row to jTable2 if employee and date month match
                                    }
                                } catch (NumberFormatException e) {
                                    // Handle invalid month format or other parsing errors
                                    e.printStackTrace();
                                }
                            }
                        }
                    }
                }
            } catch (IOException e) {
                // Handle file reading or parsing errors
                e.printStackTrace();
            }
    }//GEN-LAST:event_jComboBox1ActionPerformed

